#!/usr/bin/env node
/**
 * Draft Pattern C list-surface builder from a Drizzle table export (ADR-0026 P2).
 *
 * Usage:
 *   node scripts/gen-surface-draft.mjs \
 *     --module hrm/employee-management/employee-records-management \
 *     --surface workforce \
 *     --table hrmEmployee \
 *     --erp-module hrm --erp-object employee \
 *     --row-type EmployeeRow
 *
 * Writes `data/<surface>-list-surface.server.ts` with DRAFT header — human review required.
 * Does not overwrite an existing file unless `--force`.
 */
import fs from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..")

const SKIP_COLUMNS = new Set([
  "organizationId",
  "audit7w1h",
  "auditOrigin",
  "simulationRunId",
  "scenarioId",
  "scenarioVersion",
  "simulationSeed",
  "createdAt",
  "updatedAt",
  "createdByUserId",
  "updatedByUserId",
])

function parseArgs(argv) {
  const out = {
    module: "",
    surface: "",
    table: "",
    erpModule: "",
    erpObject: "",
    rowType: "",
    force: false,
  }
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i]
    if (a === "--module") out.module = argv[++i] ?? ""
    else if (a === "--surface") out.surface = argv[++i] ?? ""
    else if (a === "--table") out.table = argv[++i] ?? ""
    else if (a === "--erp-module") out.erpModule = argv[++i] ?? ""
    else if (a === "--erp-object") out.erpObject = argv[++i] ?? ""
    else if (a === "--row-type") out.rowType = argv[++i] ?? ""
    else if (a === "--force") out.force = true
  }
  if (
    !out.module ||
    !out.surface ||
    !out.table ||
    !out.erpModule ||
    !out.erpObject
  ) {
    console.error(
      "Required: --module --surface --table --erp-module --erp-object [--row-type] [--force]"
    )
    process.exit(1)
  }
  if (!out.rowType) {
    out.rowType = `${pascalCase(out.surface)}Row`
  }
  return out
}

function pascalCase(slug) {
  return slug
    .split(/[-_/]/u)
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join("")
}

function inferColumns(tableName) {
  const schemaPath = path.join(ROOT, "lib/db/schema.ts")
  const src = fs.readFileSync(schemaPath, "utf8")
  const marker = `export const ${tableName} = pgTable(`
  const start = src.indexOf(marker)
  if (start === -1) {
    console.error(`Table export not found: ${tableName}`)
    process.exit(1)
  }
  const bodyStart = src.indexOf("{", start)
  let depth = 0
  let bodyEnd = -1
  for (let i = bodyStart; i < src.length; i++) {
    if (src[i] === "{") depth++
    if (src[i] === "}") {
      depth--
      if (depth === 0) {
        bodyEnd = i
        break
      }
    }
  }
  const body = src.slice(bodyStart + 1, bodyEnd)
  const columns = []
  const lineRe =
    /^\s{4}(\w+):\s*(?:text|integer|boolean|timestamp|date|jsonb|uuid)/gm
  let match
  while ((match = lineRe.exec(body)) !== null) {
    const name = match[1]
    if (!SKIP_COLUMNS.has(name)) {
      columns.push(name)
    }
  }
  if (columns.length === 0) {
    console.error(`No inferable columns for ${tableName}`)
    process.exit(1)
  }
  return columns.slice(0, 6)
}

function buildFileContent({ surface, erpModule, erpObject, rowType, columns }) {
  const fn = `build${pascalCase(surface)}ListSurfaceConfiguration`
  const surfaceKey = `${erpModule}:${surface.replace(/-/g, ":")}`
  const colBlocks = columns
    .map((col) => `      { id: "${col}", header: copy.col${pascalCase(col)} },`)
    .join("\n")

  const copyKeys = columns
    .map((col) => `  col${pascalCase(col)}: string`)
    .join("\n")

  const cellLines = columns
    .map((col) => `        ${col}: String(row.${col} ?? "—"),`)
    .join("\n")

  return `import "server-only"

/**
 * DRAFT — generated by pnpm gen surface-draft. Review columns, copy, paths, and permissions before merge.
 */
import type { ListSurfaceRendererConfigurationInput } from "#features/governed-surface"

import type { ${rowType} } from "../types"

const READ_PERMISSION = {
  module: "${erpModule}" as const,
  object: "${erpObject}" as const,
  function: "read" as const,
}

const PRESENTATION = {
  variant: "table-only" as const,
  tableDensity: "comfortable" as const,
}

type ${pascalCase(surface)}ListCopy = {
  empty: string
${copyKeys}
}

export function ${fn}(
  rows: readonly ${rowType}[],
  copy: ${pascalCase(surface)}ListCopy
): ListSurfaceRendererConfigurationInput {
  return {
    __schemaVersion: 1,
    dataNature: "table",
    requiresErpPermission: READ_PERMISSION,
    presentation: PRESENTATION,
    surface: {
      header: { title: "${surfaceKey}" },
      columnsId: "${surfaceKey}",
      rowKey: "id",
      empty: {
        variant: "muted",
        title: copy.empty,
      },
    },
    columns: [
${colBlocks}
    ],
    rows: rows.map((row) => ({
      id: row.id,
      cells: {
${cellLines}
      },
    })),
  }
}
`
}

function main() {
  const args = parseArgs(process.argv.slice(2))
  const columns = inferColumns(args.table)
  const moduleRoot = path.join(ROOT, "lib/features", args.module)
  const outPath = path.join(
    moduleRoot,
    "data",
    `${args.surface}-list-surface.server.ts`
  )

  if (!fs.existsSync(moduleRoot)) {
    console.error(`Module path not found: lib/features/${args.module}`)
    process.exit(1)
  }

  if (fs.existsSync(outPath) && !args.force) {
    console.error(
      `Refusing to overwrite (use --force): ${path.relative(ROOT, outPath)}`
    )
    process.exit(1)
  }

  fs.mkdirSync(path.dirname(outPath), { recursive: true })
  const content = buildFileContent({ ...args, columns })
  fs.writeFileSync(outPath, content, "utf8")
  console.log(`Wrote draft: ${path.relative(ROOT, outPath)}`)
  console.log(`Inferred columns from ${args.table}: ${columns.join(", ")}`)
  console.log(
    "Next: wire copy in messages/en.json, section component, and human review."
  )
}

main()
