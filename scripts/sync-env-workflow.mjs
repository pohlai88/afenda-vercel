/**
 * Builds `.env.workflow.local` from `.env.local` with dev URL ports 3000 → 3002.
 * Secrets stay in `.env.local`; use `vercel env run` in dev:stack for live Vercel dev env.
 *
 * Usage: pnpm env:sync:workflow
 *        node scripts/sync-env-workflow.mjs [--force]
 */
import fs from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

import {
  AFENDA_DEV_UI_PORT,
  AFENDA_DEV_WORKFLOW_PORT,
  AFENDA_WORKFLOW_ENV_URL_KEYS,
} from "./lib/dev-stack-constants.shared.mjs"

/** @typedef {{ status: "wrote" | "skipped" | "error"; message?: string }} EnsureWorkflowEnvResult */

/**
 * @param {{ root?: string; force?: boolean; log?: (msg: string) => void }} [opts]
 * @returns {EnsureWorkflowEnvResult}
 */
export function ensureWorkflowEnvFile(opts = {}) {
  const root = opts.root ?? path.join(path.dirname(fileURLToPath(import.meta.url)), "..")
  const log = opts.log ?? console.log
  const srcPath = path.join(root, ".env.local")
  const destPath = path.join(root, ".env.workflow.local")
  const force = opts.force === true

  if (!fs.existsSync(srcPath)) {
    return {
      status: "error",
      message:
        ".env.local missing — run `pnpm env:sync` (or `pnpm env:sync:workflow`) first.",
    }
  }

  if (fs.existsSync(destPath) && !force) {
    return { status: "skipped" }
  }

  const src = fs.readFileSync(srcPath, "utf8")
  const header = `# Generated by scripts/sync-env-workflow.mjs — workflow dev (port ${AFENDA_DEV_WORKFLOW_PORT}) only. Do not commit.
# Use pnpm dev:stack; browse http://127.0.0.1:${AFENDA_DEV_WORKFLOW_PORT} for durable WDK runs.
# Secrets: prefer \`vercel env run\` in dev:stack; this file overrides origins only.

`

  const keySet = new Set(AFENDA_WORKFLOW_ENV_URL_KEYS)
  const lines = src.split(/\r?\n/)
  const out = [header]

  for (const rawLine of lines) {
    const trimmed = rawLine.trim()
    if (!trimmed || trimmed.startsWith("#")) {
      out.push(rawLine)
      continue
    }
    const eq = rawLine.indexOf("=")
    if (eq <= 0) {
      out.push(rawLine)
      continue
    }
    const key = rawLine.slice(0, eq).trim()
    let val = rawLine.slice(eq + 1)
    if (keySet.has(key) || valIncludesUiPort(val)) {
      val = rewriteDevPorts(val)
    }
    out.push(`${key}=${val}`)
  }

  fs.writeFileSync(destPath, `${out.join("\n")}\n`, "utf8")
  log(`[env:sync:workflow] wrote ${destPath}`)
  return { status: "wrote" }
}

const isDirectRun =
  process.argv[1] &&
  path.resolve(process.argv[1]) === path.resolve(fileURLToPath(import.meta.url))

if (isDirectRun) {
  main()
}

function main() {
  const force = process.argv.includes("--force")
  const result = ensureWorkflowEnvFile({ force, log: console.log })
  if (result.status === "error") {
    console.error(`[env:sync:workflow] ${result.message}`)
    process.exit(1)
  }
  if (result.status === "skipped") {
    console.log(
      "[env:sync:workflow] .env.workflow.local exists (use --force to regenerate)."
    )
  }
}

/** @param {string} val */
function valIncludesUiPort(val) {
  return (
    val.includes(`:${AFENDA_DEV_UI_PORT}`) ||
    val.includes(`localhost:${AFENDA_DEV_UI_PORT}`) ||
    val.includes(`127.0.0.1:${AFENDA_DEV_UI_PORT}`)
  )
}

/** @param {string} val */
export function rewriteDevPorts(val) {
  return val
    .replaceAll(
      `127.0.0.1:${AFENDA_DEV_UI_PORT}`,
      `127.0.0.1:${AFENDA_DEV_WORKFLOW_PORT}`
    )
    .replaceAll(
      `localhost:${AFENDA_DEV_UI_PORT}`,
      `localhost:${AFENDA_DEV_WORKFLOW_PORT}`
    )
    .replaceAll(`:${AFENDA_DEV_UI_PORT}/`, `:${AFENDA_DEV_WORKFLOW_PORT}/`)
    .replaceAll(`:${AFENDA_DEV_UI_PORT}`, `:${AFENDA_DEV_WORKFLOW_PORT}`)
}
